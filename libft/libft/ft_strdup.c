#include "libft.h"

char			*ft_strdup(const char *s1)
{
	int		i;
	char	*dup;

	i = 0;
	if ((dup = ft_strnew(ft_strlen(s1) + 1)) == NULL)
		return (NULL);
	while (s1[i] != '\0')
	{
		dup[i] = s1[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
